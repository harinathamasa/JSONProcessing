package com.csuohio;

public class Parking {

	private boolean isGarage;
	private boolean isStreet;
	private boolean isValidated;
	private boolean isLot;
	private boolean isValet;
	public boolean isGarage() {
		return isGarage;
	}
	public void setGarage(boolean isGarage) {
		this.isGarage = isGarage;
	}
	public boolean isStreet() {
		return isStreet;
	}
	public void setStreet(boolean isStreet) {
		this.isStreet = isStreet;
	}
	public boolean isValidated() {
		return isValidated;
	}
	public void setValidated(boolean isValidated) {
		this.isValidated = isValidated;
	}
	public boolean isLot() {
		return isLot;
	}
	public void setLot(boolean isLot) {
		this.isLot = isLot;
	}
	public boolean isValet() {
		return isValet;
	}
	public void setValet(boolean isValet) {
		this.isValet = isValet;
	}
	
	
	
}
